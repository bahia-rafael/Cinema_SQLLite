    private void campoSessaoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_campoSessaoItemStateChanged
        // TODO add your handling code here:
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            try {
                String valorSelecionado = evt.getItem().toString();
                String valorFormatado[] = valorSelecionado.split(" ");
                int numeroIdentificacao = Integer.parseInt(valorFormatado[valorFormatado.length - 1]);
                String horario = valorFormatado[valorFormatado.length - 5];
                String filme = valorFormatado[1];
                String disponiveis = SelectModel.verifyAvailableIngresso(numeroIdentificacao, horario, filme.replace(",", ""));

                ingressosDisponiveis.setText(disponiveis + " disponiveis");
                try {
                    qntdIngresso.setModel(new SpinnerNumberModel(1, 0, Integer.parseInt(disponiveis), 1));
                } catch (java.lang.IllegalArgumentException e) {
                    
                    qntdIngresso.setModel(new SpinnerNumberModel(0, 0, 0, 1));
                    ingressosDisponiveis.setText(disponiveis + " disponivel");
                }

            } catch (java.lang.ArrayIndexOutOfBoundsException a) {

            }
        }
    }//GEN-LAST:event_campoSessaoItemStateChanged

