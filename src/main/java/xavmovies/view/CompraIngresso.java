    private void botaoCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCadastrarActionPerformed
        // TODO add your handling code here
        String valorSelecionado = campoSessao.getSelectedItem().toString();

        String valorFormatado[] = valorSelecionado.split(" ");
        int numeroIdentificacao = Integer.parseInt(valorFormatado[valorFormatado.length - 1]);
        String horario = valorFormatado[valorFormatado.length - 5];
        String filme = valorFormatado[1];

        if (UpdateModel.vendaIngresso(numeroIdentificacao, horario, filme.replace(",", ""), Integer.parseInt(qntdIngresso.getValue().toString()))) {
            this.dispose();
        }

    }//GEN-LAST:event_botaoCadastrarActionPerformed

